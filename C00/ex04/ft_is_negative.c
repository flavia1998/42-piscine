#include <unistd.h>

void	ft_is_negative(int n)
{
	char	nb;
	char	p;

	nb = 'N';
	p = 'P';
	if (n >= 0)
	{
		write(1, &p, 1);
	}
	if (n < 0)
	{
		write(1, &nb, 1);
	}
}
