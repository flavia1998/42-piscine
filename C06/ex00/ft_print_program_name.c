#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (i < argc)
	{
		j = 0;
		while (argv[0][j] != '\0')
		{
			write (1, &argv[0][j], 1);
			j++;
		}
		i++;
	}
	return (0);
}
